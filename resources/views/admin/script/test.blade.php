17. Ngoài ra, còn có thể áp dụng một cách linh hoạt một số nhận định về Quang Dũng và thi phẩm: 
17.1. Nhà thơ Quang Dũng độc đáo một cách hồn nhiên, ông cứ sống tự nhiên như chim trên trời, cá dưới nước mà thành độc đáo. Bài thơ "Tây Tiến" hội tụ được cả cái bi, cái tráng của thời đại. Cái buồn lãng mạn của người tiểu tư sản, tiểu trí thức do biết mình được đón nhận một chân lý lớn nhưng cũng đồng thời đón nhận một gian nan lớn.
(Nhà thơ Vân Long)
17.2. Một Tây Tiến không chỉ níu kéo bước chân người lính trong nỗi niềm nhớ… Tất cả đều gợi ấn tượng của sự "lạ hóa", của những vẻ đẹp kì ảo khó gọi tên…
 (Đinh Minh Hằng)
17.3. Tây Tiến là bài thơ nổi tiếng nhất của Quang Dũng. Nó như cánh cửa dẫn dắt anh bước vào làng thơ cách mạng. Như có mối duyên ràng buộc, bài thơ gắn bó với người làm ra nó đến mức cứ nói đến Quang Dũng là người ta nhắc ngay đến Tây Tiến và ngược lại.
(Trần Lê Văn)
17.4. Tây Tiến là một bài thơ có giá trị về tư tưởng, về nghệ thuật. Bài thơ được viết ra với những màu sắc thẩm mĩ phong phú.
(Hà Đức Minh)
17.5. Tây Tiến biên cương mờ khói lửa
Quân đi lớp lớp động cây rừng
Và bài thơ ấy con người ấy
Sống mãi muôn đời với núi sông
(Giang Nam)
17.6. Tây Tiến là đoá hoa thơ vào loại đẹp nhất của thơ ca những năm tháng kháng chiến chống thực dân Pháp/ Tây Tiến là thứ quả lạ trái mùa, một "lệch chuẩn" tài hoa.
(Đỗ Kim Hồi)
17.7. Đọc Tây Tiến như ngậm âm nhạc trong miệng.
(Xuân Diệu)
17.8. Tôi làm bài thơ này rất nhanh, làm xong đọc trước đại biểu được hội, được mọi người hoan nghênh nhiệt liệt. Hồi đó tấm lòng và cảm xúc của mình ra sao thì viết vây, tôi chả chút lí luận gì về thơ cả.
 (Quang Dũng)